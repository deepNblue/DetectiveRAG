def get_all_test_cases() -> list:
    """返回所有测试用例"""
    return ALL_CASES


def get_case_by_type(case_type: str) -> list:
    """按案件类型查询"""
    return [c for c in ALL_CASES if c.case_type == case_type]


def get_case_by_difficulty(min_diff: int = 1, max_diff: int = 5) -> list:
    """按难度范围查询"""
    return [c for c in ALL_CASES if min_diff <= c.difficulty <= max_diff]


def validate_case(case: DetectiveCase) -> list:
    """验证案件数据完整性，返回错误列表"""
    errors = []
    if not case.case_id:
        errors.append("缺少case_id")
    if not case.case_type:
        errors.append("缺少case_type")
    if not (1 <= case.difficulty <= 5):
        errors.append(f"difficulty应在1-5之间，当前为{case.difficulty}")
    if len(case.case_text) < 200:
        errors.append(f"case_text应至少200字，当前{len(case.case_text)}字")
    if len(case.suspects) < 3:
        errors.append(f"至少需要3个嫌疑人，当前{len(case.suspects)}个")
    for i, s in enumerate(case.suspects):
        for field in ["name", "motive", "opportunity", "capability", "alibi"]:
            if field not in s or not s[field]:
                errors.append(f"嫌疑人{i}缺少{field}")
    if len(case.evidence) < 5:
        errors.append(f"至少需要5条证据，当前{len(case.evidence)}条")
    if len(case.timeline) < 3:
        errors.append(f"至少需要3条时间线，当前{len(case.timeline)}条")
    for field in ["culprit", "key_evidence", "reasoning"]:
        if field not in case.expected_result:
            errors.append(f"expected_result缺少{field}")
    if not case.key_clues:
        errors.append("key_clues不能为空")
    return errors


def print_summary():
    """打印所有案件摘要"""
    for c in ALL_CASES:
        errs = validate_case(c)
        status = "✅" if not errs else "❌"
        print(f"{status} {c.case_id} | {c.case_type} | 难度{'★'*c.difficulty} | "
              f"文本{len(c.case_text)}字 | 嫌疑人{len(c.suspects)}人 | "
              f"证据{len(c.evidence)}条 | 线索{len(c.key_clues)}个 | 真凶: {c.expected_result['culprit']}")
        if errs:
            for e in errs:
                print(f"   ⚠️ {e}")


